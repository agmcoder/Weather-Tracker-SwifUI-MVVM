import SwiftUI

extension Font {
    static func poppinsBold(size: CGFloat) -> Font {
        return Font.custom("Poppins-Bold", size: size)
    }
    
    static func poppinsExtraBold(size: CGFloat) -> Font {
        return Font.custom("Poppins-ExtraBold", size: size)
    }
}
